    private void limpaCampos(){
        
        jTextFieldValor.setText(StringUtil.EMPTY_FIELD);
        jTextFieldHorasTrabalhadas.setText(StringUtil.EMPTY_FIELD);
        jComboBoxProjetos.setSelectedIndex(0);
        jTextFieldValor.requestFocus();
    }
    
     private boolean validaCamposProfissionaisProjeto() {
        if(!ValidaCampos.validaValor(jTextFieldValor.getText())){
            JOptionPane.showMessageDialog(this, "Nome Inválido!");
            return false;
        }
        if(!ValidaCampos.validaHorasTrabalhadas(jTextFieldHorasTrabalhadas.getText())){
            JOptionPane.showMessageDialog(this, "Endereço Inválido!");
            return false;
        }
        return true;
    }


    private void ImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImprimirActionPerformed
       
        Connection con = ConnectionFactory.getConnection();
       
       String src = "src/reports/ListagemProfissionalProjetos.jasper";
       
       JasperPrint jasperPrint = null;
       Map parameters = new HashMap();
       parameters.put("idprofissional", this.profissional.getIdprofissional());
       
       try{
           jasperPrint = JasperFillManager.fillReport(src, parameters, con);
           
       }catch(JRException ex){
           
           System.out.println("Erro ao gerar relatório de profissionais: " + ex);
       }
       
       JasperViewer view = new JasperViewer(jasperPrint, false);
       view.setVisible(true);
    }//GEN-LAST:event_ImprimirActionPerformed

